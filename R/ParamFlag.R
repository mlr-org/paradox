#' @title Flag Parameter Object
#' @format \code{\link{R6Class}} object
#'
#' @description
#' A \code{\link[R6]{R6Class}} to represent Flag parameters.
#'
#' @section Member Variables:
#'   \emph{none}
#'
#' Inherited from \code{ParamBase}:
#' @inheritSection ParamBase Member Variables
#'
#' @section Methods:
#'   \emph{none}
#'
#' Inherited from \code{ParamBase}
#' @inheritSection ParamBase Methods
#'
#' @section Active Bindings:
#'   \emph{none}
#'
#' Inherited from \code{ParamBase}
#' @inheritSection ParamBase Active Bindings
#'
#' @return [\code{\link{ParamFlag}}].
#' @family ParamBase
#' @export
ParamFlag = R6Class("ParamFlag",
  inherit = ParamBase,
  public = list(

    # constructor
    initialize = function(id, special_vals = NULL, default = NULL, tags = NULL) {
      check = function(x, na.ok = FALSE, null.ok = FALSE) {
        if (test_special_vals(self, x))
          return(TRUE)
        check_flag(x, na.ok, null.ok)
      }

      # construct super class
      super$initialize(id = id, storage_type = "logical", check = check, special_vals = special_vals, default = default, tags = tags)
    },

    # public methods
    sampleVector = function(n = 1L) {
      sample(c(TRUE, FALSE), size = n, replace = TRUE)
    },
    denorm_vector = function(x) {
      x < 0.5 #FIXME: Do we have to take care of x==0.5?
    },
    value_to_string = function(x, short.flags=F, ...) {
      if (short.bool) {
        # Return "T" or "F"
        substr(as.character(x), 1, 1)
      } else {
        # Return "TRUE" or "FALSE"
        as.character(x)
      }
    }
  ),
  active = list(
    has_finite_bounds = function() TRUE,
    values = function() c(TRUE, FALSE),
    nlevels = function() 2L
  )
)
